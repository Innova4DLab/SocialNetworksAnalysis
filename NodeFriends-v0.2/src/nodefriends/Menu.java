    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        this.lblinfo.setText("");//Clear status information
        
        if(!isInternetReachable()){
            this.lblinfo.setText("Revisa tu conecci√≥n a internet");
            return;
        }
        
        if( txtEmail.getText().equals("Email")){
            this.lblinfo.setText("Debes escribir tu correo");
            return;
        }
        
        if(txtPassword.getText().length()<=0){
            this.lblinfo.setText("Debes escribir tu contrase√±a");
            return;
        }
         
        if(!numLevel.getText().matches("[0-9]+")){
            this.lblinfo.setText("El campo nivel solo acepta numeros");
            return;
        }
        
        if(Integer.parseInt(numLevel.getText())<=0){
            this.lblinfo.setText("El campo nivel solo acepta numeros positivos");
            return;
        }
       
        if(maxFriends.getSelectedIndex()==0){
            this.lblinfo.setText("Seleccionar el numero maximo de amigos a obtener");
            return;
        }
        
            this.lblStatus.setIcon(new ImageIcon(new ImageIcon(this.getClass().getClassLoader().getResource("images/loading.gif")).getImage().getScaledInstance(48, 64, Image.SCALE_DEFAULT)));
            
            this.jButton1.setEnabled(false);
            Thread thread = new Thread(){
            public void run(){
                 NodeFriends facebookNodes = new NodeFriends(txtEmail.getText(), txtPassword.getText(), Integer.parseInt(numLevel.getText()), Integer.parseInt(maxFriends.getSelectedItem().toString()));
                 int result = facebookNodes.run();

                 if(result==1){
                     Menu.lblinfo.setText("Datos obtenidos correctamente");
                 }else if(result==-1){
                     Menu.lblinfo.setText("Usuario o contrase√±a incorrecta");
                 }else{
                     Menu.lblinfo.setText("Ocurrio un error inesperado, vuelva a intentarlo");
                 }

                 Menu.lblStatus.setIcon(null);
                 Menu.jButton1.setEnabled(true);
                }
            };
            thread.start();
    }//GEN-LAST:event_jButton1ActionPerformed

